import java.util.Scanner;
public class Star_Pattern2 {
    public static void main(String args[])
    {
        Scanner sc=new Scanner(System.in);
        int i,j,n=5;
        for(i=1;i<=n;i++)
        {
            for(j=1;j<=2;j++)
            {
                System.out.print("* ");
            }
            System.out.println("");
        }
}   }

